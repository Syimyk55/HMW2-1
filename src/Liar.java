public class Liar {
    private String name;
    private String whereIsLiar;

    public Liar(String name, String whereIsLiar) {
        this.name = name;
        this.whereIsLiar = whereIsLiar;
    }

    public String getName() {
        return name;
    }

    public String getWhereIsLiar() {
        return whereIsLiar;
    }
}
